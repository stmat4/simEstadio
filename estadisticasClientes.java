    void consultar() {
        String sql = "SELECT * FROM facturacion";

        try {
            conect = con.getConnection();
            st = conect.createStatement();
            rs = st.executeQuery(sql);
            Object[] datos = new Object[9];
            model = (DefaultTableModel) table.getModel();
            model.setRowCount(0);

            while (rs.next()) {

                datos[0] = rs.getInt("ID");
                datos[1] = rs.getString("nombreCliente");
                datos[2] = rs.getString("apellidoCliente");
                datos[3] = rs.getString("cedulaCliente");
                datos[4] = rs.getString("graderiaSol");
                datos[5] = rs.getString("graderiaSombra");
                datos[6] = rs.getInt("palco");
                datos[7] = rs.getInt("subtotal");
                datos[8] = rs.getInt("total");
                model.addRow(datos);
            }

            table.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR" + e);
        }
    }

