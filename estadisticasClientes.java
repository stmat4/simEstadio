    public void calcular() {
        final int GRADERIA_SOL = 6000, GRADERIA_SOMBRA = 9000, PALCO_PREFERENCIAL = 25000;
        final double IVA = 0.13;

        String cantidad1Str = txtGraderiaSol.getText().trim();
        String cantidad2Str = txtGraderiaSombra.getText().trim();
        String cantidad3Str = txtPalco.getText().trim();

        try {

            int cantidad1 = cantidad1Str.isEmpty() ? 0 : Integer.parseInt(cantidad1Str);
            int cantidad2 = cantidad2Str.isEmpty() ? 0 : Integer.parseInt(cantidad2Str);
            int cantidad3 = cantidad3Str.isEmpty() ? 0 : Integer.parseInt(cantidad3Str);

            double subtotal1 = GRADERIA_SOL * cantidad1;
            double subtotal2 = GRADERIA_SOMBRA * cantidad2;
            double subtotal3 = PALCO_PREFERENCIAL * cantidad3;

            double subtotalTotal = subtotal1 + subtotal2 + subtotal3;

            double total = subtotalTotal + (subtotalTotal * IVA);

            String subtotalFormateado = String.format("%.2f", subtotalTotal);
            txtSubtotal.setText(subtotalFormateado);

            String totalFormateado = String.format("%.2f", total);
            txtTotal.setText(totalFormateado);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese solo números válidos",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
