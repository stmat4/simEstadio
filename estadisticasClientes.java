    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int fila = table.getSelectedRow();
        String ID = "", nombre = "", apellido = "";
        int cedula = 0, graderiaSol = 0, graderiaSombra = 0, palco = 0;
        double subtotal = 0.0, total = 0.0;

        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        } else {
            idc = Integer.parseInt(table.getValueAt(fila, 0).toString());
            ID = table.getValueAt(fila, 0).toString();
            nombre = table.getValueAt(fila, 1).toString();
            apellido = table.getValueAt(fila, 2).toString();
            cedula = Integer.parseInt(table.getValueAt(fila, 3).toString());
            graderiaSol = Integer.parseInt(table.getValueAt(fila, 4).toString());
            graderiaSombra = Integer.parseInt(table.getValueAt(fila, 5).toString());
            palco = Integer.parseInt(table.getValueAt(fila, 6).toString());
            subtotal = Double.parseDouble(table.getValueAt(fila, 7).toString());
            total = Double.parseDouble(table.getValueAt(fila, 8).toString());
        }
        txtID.setText(ID);
        txtNombre.setText(nombre);
        txtApellido.setText(apellido);
        txtCedula.setText("" + cedula);
        txtGraderiaSol.setText("" + graderiaSol);
        txtGraderiaSombra.setText("" + graderiaSombra);
        txtPalco.setText("" + palco);
        txtSubtotal.setText("" + subtotal);
        txtTotal.setText("" + total);
    }//GEN-LAST:event_tableMouseClicked

